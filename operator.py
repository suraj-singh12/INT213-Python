a = float(input("Enter a number: "))
b = float(input("Enter another number: "))
#Adding
print("a + b = ", a + b)

print("a + b = {:.2f}".format(a + b))
#:.2f means precision of 2 digits after decimal point

print("a - b = {:.2f}".format(a - b))
print("a * b = {:.2f}".format(a * b))
print("a / b = {:.2f}".format(a / b))
print("a // b = {:.2f}".format(a // b))
print("a ** b = {:.2f}".format(a ** b))